namespace WinUX.Controls.ScrollViewer
{
    /// <summary>
    /// Defines the enumeration values for a ScrollViewer's mode.
    /// </summary>
    public enum ScrollViewerMode
    {
        /// <summary>
        /// Scrolling disabled.
        /// </summary>
        Off,

        /// <summary>
        /// Horizontal scrolling only.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Vertical scrolling only.
        /// </summary>
        Vertical,

        /// <summary>
        /// All scrolling enabled.
        /// </summary>
        All
    }
}
namespace WinUX.Controls.ListViewBase
{
    /// <summary>
    /// Defines the enumeration values for a ListViewBase scroll to mode.
    /// </summary>
    public enum ScrollToMode
    {
        /// <summary>
        /// Scrolls to the first item in the list.
        /// </summary>
        First,

        /// <summary>
        /// Scrolls to the last item in the list.
        /// </summary>
        Last,

        /// <summary>
        /// Scrolls to a specific item in the list.
        /// </summary>
        Item
    }
}
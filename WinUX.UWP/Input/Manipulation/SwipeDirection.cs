namespace WinUX.Input.Manipulation
{
    /// <summary>
    /// Defines the enumeration values for a manipulation swipe direction.
    /// </summary>
    public enum SwipeDirection
    {
        /// <summary>
        /// No swipe.
        /// </summary>
        None,

        /// <summary>
        /// Swipe to the left.
        /// </summary>
        Left,

        /// <summary>
        /// Swipe to the right.
        /// </summary>
        Right,

        /// <summary>
        /// Swipe up.
        /// </summary>
        Up,

        /// <summary>
        /// Swipe down.
        /// </summary>
        Down
    }
}